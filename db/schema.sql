-- =========================================================================================
-- FUNCIÓN DE ROL (SEGURA)
-- =========================================================================================
CREATE OR REPLACE FUNCTION public.get_auth_role()
RETURNS integer
LANGUAGE sql
SECURITY DEFINER
AS $$
  SELECT COALESCE(
    (SELECT role_id FROM public.users WHERE id = auth.uid()),
    0
  );
$$;

