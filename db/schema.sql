-- Usuarios
CREATE POLICY user_own_data ON public.users
FOR ALL USING (id = auth.uid())
WITH CHECK (id = auth.uid());

CREATE POLICY profile_own_data ON public.profiles
FOR ALL USING (user_id = auth.uid())
WITH CHECK (user_id = auth.uid());

-- Chat
CREATE POLICY chat_own ON public.chats
FOR ALL USING (student_id = auth.uid())
WITH CHECK (student_id = auth.uid());

CREATE POLICY message_own ON public.messages
FOR ALL USING (
    EXISTS (
        SELECT 1 FROM public.chats c 
        WHERE c.id = chat_id AND c.student_id = auth.uid()
    )
);

-- Diario
CREATE POLICY diary_own ON public.diary_entries
FOR ALL USING (student_id = auth.uid())
WITH CHECK (student_id = auth.uid());

